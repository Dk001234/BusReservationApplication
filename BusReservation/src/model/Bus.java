package model;

public class Bus {
	private int bus_No;
	private int Ac;
	private int capacity;
	
	public int getBus_No() {
		return bus_No;
	}
	public void setBus_No(int bus_No) {
		this.bus_No = bus_No;
	}
	public int getAc() {
		return Ac;
	}
	public void setAc(int ac) {
		Ac = ac;
	}
	public int getCapacity() {
		return capacity;
	}
	public void setCapacity(int capacity) {
		this.capacity = capacity;
	}
}
